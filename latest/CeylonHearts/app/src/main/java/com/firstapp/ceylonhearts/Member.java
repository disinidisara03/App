package com.firstapp.ceylonhearts;

public class Member {
    private String emails;
    private String pass;
    private String imageurl;
    private String nic;
    private String samurdhi;
    private String charity;
    private String charity_cause;
    private String amount;
    private String firstname;


    public Member() {
    }

    public String getEmails()
    {
        return emails;
    }

    public void setEmails(String emails)
    {

        this.emails = emails;
    }

    public String getPass()
    {
        return pass;
    }

    public void setPass(String pass)
    {

        this.pass = pass;
    }

    public String getImageurl() {
        return imageurl;
    }

    public void setImageurl(String imageurl) {
        this.imageurl = imageurl;
    }

    public String getNic() {
        return nic;
    }

    public void setNic(String nic) {
        this.nic = nic;
    }

    public String getSamurdhi() {
        return samurdhi;
    }

    public void setSamurdhi(String samurdhi) {
        this.samurdhi = samurdhi;
    }

    public String getCharity() {
        return charity;
    }

    public void setCharity(String charity) {
        this.charity = charity;
    }

    public String getCharity_cause() {
        return charity_cause;
    }

    public void setCharity_cause(String charity_cause) {
        this.charity_cause = charity_cause;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }
}
